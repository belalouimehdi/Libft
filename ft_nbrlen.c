#include "libft.h"

short		ft_nbrlen(long nbr)
{
	short ret;

	ret = 0;
	if (nbr < 0)
		nbr = -nbr;
	while (nbr > 0)
	{
		nbr = nbr / 10;
		ret++;
	}
	return (ret);
}
